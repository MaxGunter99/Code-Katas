# There is an array with some numbers. All numbers are equal except for one. Try to find it!

def find_uniq(arr):

    count = 0
    for i in arr:
        if i not in arr[:count] + arr[count + 1:]:
            print( i )
            return i
        count += 1

    print( 'none' )
    return ''


